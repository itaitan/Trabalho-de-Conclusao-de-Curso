public class Doutor {

    private int id_doutor;
    private String nome_doutor;
    private String cro_doutor;
    private String telefone1_doutor;
    private String telefone2_doutor;
    private String especialidade_doutor;

    public int getId_doutor() {
        return id_doutor;
    }
    public void setId_doutor(int id_doutor) {
        this.id_doutor = id_doutor;
    }
    public String getNome_doutor() {
        return nome_doutor;
    }
    public void setNome_doutor(String nome_doutor) {
        this.nome_doutor = nome_doutor;
    }
    public String getCro_doutor() {
        return cro_doutor;
    }
    public void setCro_doutor(String cro_doutor) {
        this.cro_doutor = cro_doutor;
    }
    public String getTelefone1_doutor() {
        return telefone1_doutor;
    }
    public void setTelefone1_doutor(String telefone1_doutor) {
        this.telefone1_doutor = telefone1_doutor;
    }
    public String getTelefone2_doutor() {
        return telefone2_doutor;
    }
    public void setTelefone2_doutor(String telefone2_doutor) {
        this.telefone2_doutor = telefone2_doutor;
    }
    public String getEspecialidade_doutor() {
        return especialidade_doutor;
    }
    public void setEspecialidade_doutor(String especialidade_doutor) {
        this.especialidade_doutor = especialidade_doutor;
    }

}
